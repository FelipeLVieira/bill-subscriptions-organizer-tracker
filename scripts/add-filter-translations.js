function addTranslationsToFile(filename, translations) {
    const filepath = path.join(LOCALES_DIR, filename);
    if (!fs.existsSync(filepath)) {
        return false;
    }

    let content = fs.readFileSync(filepath, 'utf-8');

    // Find the closing brace and add translations before it
    const closingIndex = content.lastIndexOf('};');
    if (closingIndex === -1) return false;

    // Build new translations string
    const newTranslations = Object.entries(translations)
        .map(([key, value]) => `    ${key}: "${value.replace(/"/g, '\\"')}",`)
        .join('\n');

    // Insert translations before closing brace
    content = content.slice(0, closingIndex) +
        `    // Filter & View\n${newTranslations}\n` +
        content.slice(closingIndex);

    fs.writeFileSync(filepath, content);
    return true;
}

