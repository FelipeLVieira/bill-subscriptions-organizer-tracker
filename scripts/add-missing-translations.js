// Add missing translations to a locale file
function addTranslationsToFile(filename, newTranslations) {
    const filepath = path.join(LOCALES_DIR, filename);
    if (!fs.existsSync(filepath)) {
        console.log(`  ⚠️  File not found: ${filename}`);
        return false;
    }

    let content = fs.readFileSync(filepath, 'utf-8');

    // Find the closing bracket of the export
    const closingBracket = content.lastIndexOf('};');
    if (closingBracket === -1) {
        console.log(`  ⚠️  Could not find closing bracket in ${filename}`);
        return false;
    }

    // Build new translations string
    const newLines = Object.entries(newTranslations)
        .map(([key, value]) => `    ${key}: "${value.replace(/"/g, '\\"')}",`)
        .join('\n');

    // Insert before closing bracket
    const before = content.slice(0, closingBracket);
    const after = content.slice(closingBracket);

    // Check if we need a comma after the last existing entry
    const needsComma = !before.trim().endsWith(',');

    content = before + (needsComma ? ',' : '') + '\n' + newLines + '\n' + after;

    fs.writeFileSync(filepath, content);
    return true;
}

// Main function
function main() {
    console.log('📝 Adding Missing Translations\n');
    console.log('='.repeat(60) + '\n');

    let successCount = 0;
    let skipCount = 0;

    for (const [locale, translations] of Object.entries(TRANSLATIONS)) {
        const filename = `${locale}.ts`;
        console.log(`Processing ${filename}...`);

        if (Object.keys(translations).length === 0) {
            console.log(`  ⏭️  No translations to add`);
            skipCount++;
            continue;
        }

        if (addTranslationsToFile(filename, translations)) {
            console.log(`  ✅ Added ${Object.keys(translations).length} translations`);
            successCount++;
        }
    }

    console.log('\n' + '='.repeat(60));
    console.log(`\n📊 Summary:`);
    console.log(`   - Files updated: ${successCount}`);
    console.log(`   - Files skipped: ${skipCount}`);
    console.log('\nRun "node scripts/check-translations.js" to verify.\n');
}

main();
